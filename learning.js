// What is typescript?
// Addresses shortcommings of JS
// - adds discipline
// - built on top of JS
// - adds:
//     - *static typing*
//     - code completion
//     - refactoring
//     - shorthand notations
// two types of languages:
// - statically typed (c++, c#, Java)
//     - know type of variables at compile type or in editor
//     - can't reset a variable to a differet type
// - dynamically-typed (JS, Python, Ruby)
//     - determined at compile type and can also change the type
//      - causes issues that we won't determine type misuse until run time

// typescript == javascript with type checking
// drawbacks
//      - alwyas needs a step of compilation (transpilation)
//      - must be a disciplined coder

